
public class Swap {
	
	public static void main(String[] args) {
		int x=2;
		int y=3;
		System.out.println(x);
		System.out.println(y);
		x=x+y;//2+3=5
		y=x-y;//5-3=2 //swap
		x=x-y;//5-2=3 //swap 
		System.out.println("****************");
		System.out.println(x);
		System.out.println(y);
		
	}

}
